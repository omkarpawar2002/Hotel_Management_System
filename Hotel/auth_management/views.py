from django.shortcuts import render,redirect
from django.contrib.auth.forms import UserCreationForm
from django.http import HttpResponse
from django.contrib.auth import authenticate,login,logout

# Create your views here.
def sign_up(request):
    form = UserCreationForm()
    if(request.method == 'POST'):
        form = UserCreationForm(request.POST)
        if(form.is_valid()):
            form.save()
            return redirect('sign_in')
        else:
            return HttpResponse("Something Went Wrong While Registering!!")
    template_name = 'auth_management/sign_up.html'
    context = {'form':form}
    return render(request,template_name,context)

def sign_in(request):
    if(request.method == 'POST'):
        username = request.POST.get('username')
        password = request.POST.get('password')
        user = authenticate(username=username,password=password)
        if(user is not None):
            login(request,user)
            return redirect('show_bookings')
        else:
            return HttpResponse("Something Will Wrong While Log In!!")
    template_name = 'auth_management/sign_in.html'
    context = {}
    return render(request,template_name,context)

def sign_out(request):
    logout(request)
    return redirect('sign_up')